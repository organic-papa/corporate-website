		}).done(function(data) {
			$('.js-pref').val(data.code);
			$('.js-addr01').val(data.city + data.town);
		}).fail(function(data) {
			$('.js-error-zip01').text('住所の自動入力に失敗しました');
		});
	});

	// submit data.
	$('.js-submit').on('click', function() {
		var $button = $(this);
		var $loading = $('#loading');

		$('.errorMsg').empty();
		$button.hide();
		$loading.show().css({padding: '20px'}).activity({
			segments: 12,
			width: 6,
			space: 4,
			length: 1,
			color: '#000',
			speed: 1.5
		});

		var data = {};
		var $form = $('.js-register-form');
		$form.find(':input').each(function(elem) {
			data[$(this).attr('name')] = $(this).val();
		});

		$.ajax({
			'type': 'POST',
			'url': '/regular/register',
			'data': data
		}).done(function(msg, st, xhr) {
			$loading.hide().css({padding: '0'});
			$('.finishMsg').show();
		}).fail(function(xhr, st, error) {
			var position = 0;

			var res = eval('(' + xhr.responseText + ')');
			_.each(res.messages, function(val, key) {
				var $err = $('.js-error-' + key);
				$err.text(val[0]);

				if (position == 0 && $err.length > 0) {
					position = $err.offset().top - 50;
				}
			});

			if (position != 0) {
				$('html,body').animate({ scrollTop: position }, 'fast');
			}

			$loading.hide().css({padding: '0'});
			$button.show();
		});
	});

});
